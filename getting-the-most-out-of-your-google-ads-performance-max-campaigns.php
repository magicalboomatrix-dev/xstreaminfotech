					 <h2>Common pitfalls with Performance Max and how to avoid them</h2>
					 <p>Even experienced marketers will encounter obstacles when managing complex campaigns. From overlooking crucial aspects like conversion tracking to inadvertently focusing budgets towards branded traffic, the pitfalls can be diverse and challenging. Here are some common pitfalls in Performance Max campaigns and some tips and strategies for avoiding them.</p>
					 <h3>Overlooking conversion tracking</h3>
					 <p>One critical mistake is failing to set up conversion tracking correctly from the outset. Accurate tracking is essential for measuring the effectiveness of your campaigns and making informed decisions. This applies equally to SEO and other PPC campaigns. As Performance Max campaigns aim to get you the most conversions for your budget, it’s important that you have linked the right conversion action and that it is firing correctly. This ensures that your campaign is optimising towards your business goals and not any secondary conversion actions.  </p>
					 <h3>Not segmenting and managing branded terms</h3>
					 <p>Because Performance Max campaigns have a goal of getting you the most conversions for your budget, if your brand generates a lot of branded search volume in Google, there’s a high likelihood your Performance Max campaign will end up spending its budget on branded traffic. This is because branded traffic is much more likely to convert than non-branded traffic as these users are already familiar with your brand.<p>
					 <p>If you already have a separate branded search campaign, you can fill out the Performance Max Campaign Modification Request Form to have your brand terms excluded from your Performance Max campaign. Otherwise, if you don’t want to target branded traffic at all, you can add your brand keywords to the Account Wide Negative Keyword list under account settings. <p>
					 <p>Managing your branded terms is an important way of ensuring your Performance Max campaigns are optimising for new customer acquisition and not relying on getting conversions that otherwise would have gone through your branded search campaign or organic listings in the search results.<p>
					 <h3>Failing to optimise your product feed</h3>
					 <p>In ecommerce, the product feed plays a crucial role in dictating the keywords that trigger your product ads. When running Google Shopping campaigns, you don’t manually select the keywords for your products; instead, Google matches your products with relevant searches based on your product feed information.<p>
					 <p>To ensure the shopping ads within your Performance Max campaign show for the most relevant keywords, make sure your product title and description are relevant and descriptive to what the product is. If these are too vague, you risk your shopping ads showing for irrelevant keywords. <p>
					 <p>A handy tip is to search the keywords you want certain products to show for, and look at the product titles and descriptions of the top shopping listings. This should give you inspiration of how you should structure your product feed information for success.<p>
					 <p><i>To ensure the shopping ads within your Performance Max campaign show for the most relevant keywords, make sure your product title and description are relevant and descriptive to what the product is.
</i><p>
					 <h3>Not adding video creative to your asset groups</h3>
					 <p>As Performance Max campaigns utilise all of Google Ads’ advertising placements, one of the most problematic parts of setting one up is not providing a high quality video for each of your asset groups for video placements. This is because without providing your own videos, Google will create one for you. The issue here is that you have no control over how these videos look, and they leave a lot to be desired.</p>

					 <p>If you don’t want Google auto-generating a video creative for you that will likely be very off-brand for your business, you can use free tools like Canva to create your own quickly and easily. </p>
					 
					 <h3>Not managing final URL expansion</h3>
					 <p>Keeping in line with Google’s drive for automation, there is a setting within Performance Max campaigns called “Final URL Expansion”. If this is switched on, you’re giving Google the power to send users to different pages on your website than the final URL you provide, if Google thinks a different URL will provide better performance. </p>
					 <p>The issue here is that Google may lead users through to a page that you don’t want them to. To properly manage this, you need to set exclusions for all URLs you don’t want Google to lead users through. </p>
					 <p>When considering whether you want to use Final URL expansion, ensure you have a rigorous exclusion list for the campaign so that traffic isn’t filtered through to any non-commercial pages on your website. 
</p>
